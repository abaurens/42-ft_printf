#include <stdint.h>

void			ft_swap(void *a, void *b)
{
	uintptr_t	*aa;
	uintptr_t	*bb;

	aa = *((uintptr_t **)a);
	bb = *((uintptr_t **)b);
	*aa ^= (*bb ^= (*aa ^= *bb));
}
